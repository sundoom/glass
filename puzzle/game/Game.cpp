static GImagePtr s_image_ptr;
static GPoint s_pt;

//////////////////////////////////////////////////////////////////////////

/*
====================
GInitGame
====================
*/
VOID GInitGame()
{
  s_image_ptr = GImage::Load(GLoad("image/mm16.tga"));
}

/*
====================
GRunGame
====================
*/
VOID GRunGame(F64 delta_time)
{
  s_pt.X() += 1;
  s_pt.Y() += 1;
  s_pt.X() = s_pt.X() % 800;
  s_pt.Y() = s_pt.Y() % 600;
}

/*
====================
GDrawGame
====================
*/
VOID GDrawGame(GGraph& graph)
{
  graph.Draw(s_pt, s_image_ptr.Get());
}

